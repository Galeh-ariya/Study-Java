public class OperasiPerbandingan {
    public static void main(String[] args) {

        int angka1 = 8;
        int angka2 = 10;

        System.out.println(angka1 > angka2);
        System.out.println(angka1 < angka2);
        System.out.println(angka1 <= angka2);
        System.out.println(angka1 >= angka2);
        System.out.println(angka1 == angka2);
        System.out.println(angka1 != angka2);

    }
}
